type BuildWalletTopUpCheckoutSessionParamsArgs = {
  currency: string;
  settlementAmountCents: number;
  successUrl: string;
  cancelUrl: string;
  sessionMetadata: Record<string, string>;
  paymentIntentMetadata: Record<string, string>;
  productTaxCode: string;
  connectTransfer?: {
    destinationAccountId: string;
    applicationFeeAmount: number;
  } | null;
};

export function buildWalletTopUpCheckoutSessionParams({
  currency,
  settlementAmountCents,
  successUrl,
  cancelUrl,
  sessionMetadata,
  paymentIntentMetadata,
  productTaxCode,
  connectTransfer = null,
}: BuildWalletTopUpCheckoutSessionParamsArgs): Stripe.Checkout.SessionCreateParams {
  const paymentIntentData: Stripe.Checkout.SessionCreateParams.PaymentIntentData = {
    metadata: paymentIntentMetadata,
  };

  if (connectTransfer) {
    paymentIntentData.application_fee_amount = connectTransfer.applicationFeeAmount;
    paymentIntentData.transfer_data = {
      destination: connectTransfer.destinationAccountId,
    };
  }

  return {
    mode: 'payment',
    billing_address_collection: 'auto',
    automatic_tax: { enabled: true },
    tax_id_collection: { enabled: true },
    shipping_address_collection: {
      allowed_countries: WALLET_TOPUP_SHIPPING_ADDRESS_COUNTRIES,
    },
    custom_text: {
      shipping_address: {
        message: 'Used only to confirm tax location for this digital wallet top-up.',
      },
    },
    success_url: successUrl,
    cancel_url: cancelUrl,
    line_items: [
      {
        price_data: {
          currency,
          product_data: { name: 'Wallet top-up', tax_code: productTaxCode },
          unit_amount: settlementAmountCents,
          tax_behavior: 'exclusive',
        },
        quantity: 1,
      },
    ],
    metadata: sessionMetadata,
    payment_intent_data: paymentIntentData,
  };
}
